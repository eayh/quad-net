class reconstruct_filter(nn.Module):
    def __init__(
        self,
        kernel_size=64,
        padding=0,
        num_heads=1,

    ):
        super().__init__()
        self.kernel_size = kernel_size
        self.num_heads = num_heads
        self.padding = padding
        self.weight = nn.Parameter(torch.Tensor(num_heads, 1, kernel_size))


        self.reset_parameters()

    def reset_parameters(self):
        nn.init.xavier_uniform_(self.weight)
    def qmf(self,weight):


        c = (torch.flip(weight, [2]))
        for i in range(c.size(2)):
            if i % 2 == 0:
                c[:, :, i] = -c[:, :, i]
        return c

    def forward(self, input):
        """
        input size: B x 2 x T
        output size: B x 2 x T
        """
        B, C, T = input.size()
        H = self.num_heads
        input1=input[:,0:1,:]
        input2=input[:,1:2,:]

        weight = self.weight
        #normalizing weight
        weight = weight/(torch.sum(weight**2)**0.5)

        # Merge every C/H entries into the batch dimension (C = self.input_size)
        # B x C x T -> (B * C/H) x H x T
        # One can also expand the weight to C x 1 x K by a factor of C/H
        # and do not reshape the input instead, which is slow though
        # zero pad with
        input1 = torch.nn.functional.conv_transpose1d(input1, torch.tensor([[[1, 0]]]).float().cuda(), stride=2)
        input2 = torch.nn.functional.conv_transpose1d(input2, torch.tensor([[[1, 0]]]).float().cuda(), stride=2)

        output1 = F.conv1d(input1, weight, padding=self.padding, groups=self.num_heads)
        output2 = F.conv1d(input2, self.qmf(weight), padding=self.padding, groups=self.num_heads)
        output=output1+output2


        return output, (weight,self.qmf(weight))


def weightloss(weight):
    weight=weight[0]
    L=weight.size(2)
    sum=0
    for i in range(L//2-1):
        i=i+1
        pad_weight=torch.nn.functional.pad(weight[:,:,2*i:],(0,2*i))
        temp=F.conv1d(weight, pad_weight).squeeze()
        sum=sum+temp**2
    return sum
class ResBlock1(torch.nn.Module):
    def __init__(self, h, channels, kernel_size=3, dilation=(1, 3, 5)):
        super(ResBlock1, self).__init__()
        self.h = h
        self.convs1 = nn.ModuleList([
            weight_norm(Conv1d(channels, channels, kernel_size, 1, dilation=dilation[0],
                               padding=get_padding(kernel_size, dilation[0]))),
            weight_norm(Conv1d(channels, channels, kernel_size, 1, dilation=dilation[1],
                               padding=get_padding(kernel_size, dilation[1]))),
            weight_norm(Conv1d(channels, channels, kernel_size, 1, dilation=dilation[2],
                               padding=get_padding(kernel_size, dilation[2])))
        ])
        self.convs1.apply(init_weights)

        self.convs2 = nn.ModuleList([
            weight_norm(Conv1d(channels, channels, kernel_size, 1, dilation=1,
                               padding=get_padding(kernel_size, 1))),
            weight_norm(Conv1d(channels, channels, kernel_size, 1, dilation=1,
                               padding=get_padding(kernel_size, 1))),
            weight_norm(Conv1d(channels, channels, kernel_size, 1, dilation=1,
                               padding=get_padding(kernel_size, 1)))
        ])
        self.convs2.apply(init_weights)

    def forward(self, x):
        for c1, c2 in zip(self.convs1, self.convs2):
            xt = F.leaky_relu(x, LRELU_SLOPE)
            xt = c1(xt)
            xt = F.leaky_relu(xt, LRELU_SLOPE)
            xt = c2(xt)
            x = xt + x
        return x

    def remove_weight_norm(self):
        for l in self.convs1:
            remove_weight_norm(l)
        for l in self.convs2:
            remove_weight_norm(l)


